#сумма всех чисел
import random


print(40 *"=")
a=[[1,1,1],[7,7,7],[9,9,9],]
N=3
M=3
for i in range(N): 
    for j in range(M): 
       a[i][j] = random.randint ( 10, 60 )
       print ( "{:4d}".format(a[i][j]), end = "" ) 
    print()
    print(sum(a[i]))

print(40 *"=")

